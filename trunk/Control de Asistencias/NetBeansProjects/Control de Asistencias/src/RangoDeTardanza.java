public class RangoDeTardanza {
   private int cdadDiasPerdon;
   private int minutosDesde;
   private int minutosHasta;
   private float porcentajeDescuento;

   public int getCdadDiasPerdon() {
      return cdadDiasPerdon;
   }

   public void setCdadDiasPerdon(int cdadDiasPerdon) {
      this.cdadDiasPerdon = cdadDiasPerdon;
   }

   public int getMinutosDesde() {
      return minutosDesde;
   }

   public void setMinutosDesde(int minutosDesde) {
      this.minutosDesde = minutosDesde;
   }

   public int getMinutosHasta() {
      return minutosHasta;
   }

   public void setMinutosHasta(int minutosHasta) {
      this.minutosHasta = minutosHasta;
   }

   public float getPorcentajeDescuento() {
      return porcentajeDescuento;
   }

   public void setPorcentajeDescuento(float porcentajeDescuento) {
      this.porcentajeDescuento = porcentajeDescuento;
   }
} // fin de la clase RangoDeTardanza